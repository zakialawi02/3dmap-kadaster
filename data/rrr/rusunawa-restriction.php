    <li><strong>Penyewa</strong>
        <ol style="list-style-type:upper-latin;">
            <li>Surat Perjanjian Sewa Menyewa<ol style="list-style-type:decimal;">
                    <li>Sewa adalah penggunaan barang milik daerah dengan imbalan uang tunai dalam periode tertentu. <strong>Penyewa adalah individu yang membayar biaya sewa dan telah disetujui oleh pengelola RUSUNAWA untuk waktu &nbsp;tinggal sesuai perjanjian.</strong></li>
                    <li><strong>Penyewa dikhususkan WNI yang berdomisili di wilayah Kota Malang</strong>, serta termasuk dalam <strong>kelompok masyarakat berpenghasilan rendah</strong> sesuai Pasal 1 ayat (14) Peraturan Walikota Nomor 41 Tahun 2013 tentang &nbsp;Tata Cara Pengelolaan Rumah Susun Sederhana Sewa.</li>
                    <li><strong>Perjanjian sewa berlaku hingga batas waktu yang telah ditentukan</strong>. Apabila penyewa berniat untuk memperpanjang perjanjian, mereka diharuskan untuk mengajukan izin kepada pengelola paling lambat satu bulan sebelum periode sewa berakhir. Pada saat perjanjian berakhir, penyewa wajib untuk mengosongkan unit hunian dalam kondisi baik dan menyerahkannya kembali kepada pengelola paling lambat 7 hari kerja sebelumnya.</li>
                    <li><strong>Hunian hanya diperkenankan dihuni satu keluarga</strong> sebagaimana dibuktikan dengan daftar nama pada kartu keluarga.</li>
                    <li>Pengerjaan peralatan, <strong>perbaikan/renovasi yang bersifat umum, harus seijin pengelola</strong>.</li>
                    <li>Penghuni/tamu yang membawa <strong>kendaraan</strong> menempatkan pada tempat <strong>parkir/lokasi yang telah ditetapkan.</strong></li>
                    <li>Hewan peliharaan lain diluar yang dilarang dalam ketentuan perjanjian sewa ini dapat dipelihara dengan seijin pengelola.</li>
                    <li>Selama masa berlakunya perjanjian sewa, penyewa dilarang melakukan berbagai tindakan seperti menyewakan atau memindahkan hak sewa kepada pihak lain, memperjualbelikan unit hunian, atau menggunakan hunian untuk kegiatan usaha. Larangan juga termasuk melakukan perubahan pada bangunan, menyimpan bahan berbahaya, atau melakukan kegiatan yang mengganggu ketertiban lingkungan. Pelanggaran terhadap ketentuan ini dapat mengakibatkan sanksi administratif atau bahkan hukuman pidana sesuai dengan peraturan yang berlaku.</li>
