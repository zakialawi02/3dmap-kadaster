    <li><strong>Penyewa</strong>
        <ol style="list-style-type:upper-latin;">
            <li>Surat Perjanjian Sewa Menyewa<ol style="list-style-type:decimal;">
                    <li>Sewa adalah pemanfaatan barang milik daerah oleh pihak lain dalam jangka waktu tertentu dengan menerima imbalan uang tunai;</li>
                    <li>Biaya RUSUNAWA adalah semua biaya termasuk rekening PLN, rekening PDAM, dan iuran pengelolaan yang harus dibayar oleh penyewa/penghuni;</li>
                    <li>Besaran sewa untuk rumah susun sederhana sewa ditetapkan berdasarkan Peraturan Walikota Malang Nomor 41 Tahun 2013. Besaran sewa tersebut belum termasuk biaya lain yang terkait dengan pengelolaan RUSUNAWA.</li>
                    <li>Pihak kedua, sebagai penyewa, memiliki kewajiban yang harus dipatuhi secara bertanggung jawab. Mereka diharuskan mematuhi peraturan dan tata tertib penghuni yang ditetapkan oleh pengelola rumah susun. Selain itu, penyewa bertanggung jawab menjaga kebersihan unit hunian dan fasilitas umum, membayar sewa dan biaya lainnya sesuai ketentuan, serta melaporkan kerusakan atau masalah lainnya kepada pengelola. Penyewa juga harus mengosongkan unit hunian tepat waktu saat perjanjian berakhir, serta berpartisipasi dalam menciptakan lingkungan yang harmonis dan mengikuti sosialisasi yang diadakan oleh pengelola. Selain itu, mereka diharapkan mematuhi aturan terkait penempatan kendaraan, pelaporan kedatangan tamu, dan pengajuan izin untuk kegiatan yang dapat mengganggu ketertiban lingkungan.</li>
                    <li>Jika penyewa bermaksud memperpanjang perjanjian sewa setelah berakhirnya masa perjanjian yang disebutkan dalam ayat sebelumnya, mereka diharuskan untuk mengajukan izin penggunaan fasilitas hunian kepada pengelola rumah susun paling lambat satu bulan sebelum periode perjanjian berakhir.</li>
                    <li>Jika perjanjian berakhir atau terhenti karena alasan apapun, penyewa wajib mengosongkan dan menyerahkan unit hunian dalam kondisi baik kepada pengelola rumah susun paling lambat 7 hari kerja sebelum berakhirnya perjanjian.</li>
                    <li>Pihak penyewa wajib melaporkan perubahan dalam daftar penghuni kepada pengelola rumah susun apabila terjadi perubahan dalam kartu keluarga penghuni yang bersangkutan.</li>
                    <li>Penghuni, sebagai penyewa, memiliki kewajiban-kewajiban seperti melaporkan perubahan dalam daftar penghuni, menciptakan lingkungan yang aman dan bersih, serta mematuhi semua peraturan yang ditetapkan oleh pengelola. Mereka juga harus mematikan perangkat elektronik saat meninggalkan hunian, menjaga ketenangan lingkungan, dan melaporkan jika akan meninggalkan hunian untuk sementara waktu. Selain itu, penghuni diwajibkan mematuhi peraturan parkir, serta meminta izin untuk peralatan dan renovasi umum.</li>
                </ol>
